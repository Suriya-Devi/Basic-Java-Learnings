package javabasics;

public class StaticMethods {
	static void add() {
		
	System.out.println("Addition");	
	}
	
	public static void main(String[] args) {
	add();
		subtract();
		multiply();
	}
	
	static void subtract() {
		System.out.println("Subtraction");
		
	}
	
	static void multiply() {
		
		System.out.println("Mutliplication");
		
	}

}
